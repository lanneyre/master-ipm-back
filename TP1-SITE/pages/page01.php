<h2>Introduction</h2>

<p>La statistique descriptive étudie à l'heure actuelle les
    données qui peuvent être recueillies sur des groupements d'éléments très
    divers. Ainsi on peut utiliser les méthodes de cette statistique pour décrire
    les informations recueillies sur un groupe d'élèves mais aussi sur un groupe de
    textes, d'hôpitaux, d'écoles, d'épidémies etc. Cependant elle a eu pour objet
    initial des questions démographiques et politiques, Les premiers groupements
    étudiés ont été naturellement dénommés <em>population</em> et les éléments qui
    le composent <em>individus</em>. Ce vocabulaire initial a été conservé. Quel
    que soit le type d'objets étudiés ils seront invariablement désignés par le
    terme <em>individus</em> et leur ensemble par celui de <em>population</em>.
    Une description statistique commence par l'identification de cette population
    et des individus qui la composent.</p>

<h5>En voici un exemple:</h5>

<p class=example>&quot;Les données dont le traitement est discuté dans ce
    paragraphe ont été obtenues à l'aide d'un questionnaire passé auprès de 126
    élèves de 6 classes (deux classes de Troisième, une classe de seconde une
    classe de Première et deux Terminales) ayant pratiqué le logiciel DERIVE pendant
    au moins 7 heures&quot;.</p>
<ul>
    <li>Cette introduction pose la population et les individus
        étudiés: les 126 questionnaires (population qui sera parfois abusivement
        assimilée à celle des 126 élèves).</li>

    <li>Cette introduction précise le nombre total d'individus qui
        composent la population (dans l'exemple 126). Ce nombre est l'effectif total de
        la population, il est le plus souvent désigné par la lettre N dans l'écriture
        formelle de description.</li>
</ul>